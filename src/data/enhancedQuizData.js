// Helper function untuk mendapatkan questions berdasarkan quizId
export const getQuizQuestions = (quizId) => {
  return enhancedQuizQuestions[quizId] || [];
};

// Function untuk mendapatkan total questions count
export const getQuizQuestionsCount = (quizId) => {
  return enhancedQuizQuestions[quizId] ? enhancedQuizQuestions[quizId].length : 0;
};

// Function untuk mendapatkan quiz duration berdasarkan timeLimit questions
export const getQuizDuration = (quizId) => {
  const questions = enhancedQuizQuestions[quizId];
  if (!questions || questions.length === 0) return 15; // default duration
  
  const totalTime = questions.reduce((sum, question) => sum + (question.timeLimit || 30), 0);
  return Math.ceil(totalTime / 60); // convert to minutes
};

// Function untuk mendapatkan quiz difficulty level
export const getQuizDifficulty = (quizId) => {
  const questions = enhancedQuizQuestions[quizId];
  if (!questions || questions.length === 0) return 'Pemula';
  
  const difficulties = questions.map(q => q.difficulty);
  const difficultyCount = {
    easy: difficulties.filter(d => d === 'easy').length,
    medium: difficulties.filter(d => d === 'medium').length,
    hard: difficulties.filter(d => d === 'hard').length
  };
  
  if (difficultyCount.hard > difficultyCount.medium && difficultyCount.hard > difficultyCount.easy) {
    return 'Lanjutan';
  } else if (difficultyCount.medium >= difficultyCount.easy) {
    return 'Menengah';
  } else {
    return 'Pemula';
  }
};

// Function untuk mendapatkan quiz categories
export const getQuizCategories = (quizId) => {
  const questions = enhancedQuizQuestions[quizId];
  if (!questions) return [];
  
  const categories = [...new Set(questions.map(q => q.category))];
  return categories;
};
